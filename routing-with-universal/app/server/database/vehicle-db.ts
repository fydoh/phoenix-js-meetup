    getVehicles(color: string) {
        let matchedVehicles = this.filterVehiclesByColor(color);
        let preformattedVehicles = this.createColumnsAndRows(matchedVehicles); // This belongs in the UI
        return preformattedVehicles;
    }

    private filterVehiclesByColor(color: string) {
        let vehiclesThatMatch = [];

        if (color) {
            vehiclesThatMatch = this.vehicles.filter((vehicle) => {
                return vehicle.color === color;
            });
        } else {
            vehiclesThatMatch = this.vehicles;
        }

        return vehiclesThatMatch;
    }

    private createColumnsAndRows(vehicles) {
        let groupSize = 3;
        let groups = vehicles.map((item, start) => {
            if (start % groupSize === 0) {
                return vehicles.slice(start, start + groupSize);
            }
        }).filter((isItem) => { return isItem; });

        return groups;
    }

    getVehicle(id: number) {
        return this.filterVehiclesById(id);
    }

    private filterVehiclesById(id: number) {
        let matchId = Number(id);
        let vehiclesThatMatch = this.vehicles.filter((vehicle) => {
            return vehicle.mileage === matchId;
        });

        return vehiclesThatMatch[0];
    }
